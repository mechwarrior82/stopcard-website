<div class="faq_title" name="rfid"><div class="title_text">How Does RFID Work?</div><img class="rfid" src="/images/chevron_down1.png"/><div class="clear"></div></div>
<div class="faq_text rfid_text">When your RFID enabled credit/debit card is placed in the vicinity of a RFID reader, the reader emits a specific frequency that makes the RFID enabled smart card broadcast all of your personal information in order to complete the requested transaction.</div>

<div class="faq_title" name="work"><div class="title_text">How does the STOPCARD<sup>&reg;</sup> work?</div><img class="work" src="/images/chevron_down1.png"/><div class="clear"></div></div>
<div class="faq_text work_text">The STOPCARD<sup>&reg;</sup> creates an electronic shield around your RFID enabled credit/debit cards and I.D.s. This shield prevents your RFID enabled cards from responding to an RFID reading device.</div>

<div class="faq_title" name="type"><div class="title_text">What type of cards does it work on?</div><img class="type" src="/images/chevron_down1.png"/><div class="clear"></div></div>
<div class="faq_text type_text">Any RFID device that uses the 13.56 MHz frequency. This includes US passports and all RFID credit and Debit Cards. It can even protect your security ID badge used for contactless secure entry systems.</div>

