<div class="faq_title" name="range"><div class="title_text">What is its protection range?</div><img class="range" src="/images/chevron_down1.png"/><div class="clear"></div></div>
<div class="faq_text range_text">Protects typical credit card distribution within your wallet. So a single STOPCARD<sup>&reg;</sup> properly placed in proximity of the cards in your wallet can protect all cards in the wallet.</div>

<div class="faq_title" name="legal"><div class="title_text">Is it legal to use the STOPCARD<sup>&reg;</sup>?</div><img class="legal" src="/images/chevron_down1.png"/><div class="clear"></div></div>
<div class="faq_text legal_text"> The STOPCARD<sup>&reg;</sup> complies with US FCC requirements for Passive RFID devices.  It is protected by U.S. Patents 7889056, 8102243, 8237549, 8305193.<br /><br />Other U.S. and International Patents are pending.</div>

<div class="faq_title" name="difference"><div class="title_text">What is the difference between the STOPCARD<sup>&reg;</sup> and a basic foil sleeve?</div><img class="difference" src="/images/chevron_down1.png"/><div class="clear"></div></div>
<div class="faq_text difference_text">The STOPCARD<sup>&reg;</sup> is an unbeatable Active countermeasure system.<br />
<i>A basic foil sleeve only has a Passive defense system.</i>
<br /><br />
The STOPCARD<sup>&reg;</sup> allows you to see all your credit cards for ease of access when needed. <br />
<i>A basic foil sleeve covers each credit card and can make it difficult to find the card to use.</i>
<br /><br />
One STOPCARD<sup>&reg;</sup> works on Credit Cards, ID Cards and Passports. Regardless of shape.<br />
<i>A basic sleeve must match shape of item and only works if it properly fits firmly to the RFID device.</i>
<br /><br />
The STOPCARD<sup>&reg;</sup> has an extended range that works on all cards within proximity of the STOPCARD<sup>&reg;</sup> <br />
<i>A basic sleeve only works on direct contact.</i>
<br /><br />
The STOPCARD<sup>&reg;</sup> has a Detection Light that tells you when an RFID transmitter is trying to read your card. <br />
<i>A basic sleeve has no detection.</i>
<br /><br />
The STOPCARD<sup>&reg;</sup> requires no effort to use. Place it in your wallet and you are protected.<br />
<i>A basic sleeve must be repositioned every time the card it is protecting is used. This is due to the direct contact requirement.</i></div>

